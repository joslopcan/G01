package main;

public class MathMethods {
	
	public static Integer sum(Integer a, Integer b){
		Integer result;
		result = a + b;
		return result;
	}
	
	public static Integer prod(Integer a, Integer b){
		Integer result;
		result = a * b;
		return result;
	}

	public static Integer div(Integer a, Integer b){
		Integer result;
		result = a / b;
		return result;
	}

	public static Integer primo(Integer a){
		System.out.println("Eres un primo");
		return a;

	}
	
}
